	       <h1><font color="red">Introduction to the Language of Kinematics</font></h1>
	       <p><div style="font-family:Times news roman; font-size: 15px;"><a name="p2"></a><span class="boldred">Kinematics</span> is the science of describing the motion of objects using words, diagrams, numbers, graphs, and equations. Kinematics is a branch of mechanics. The goal of any study of kinematics is to develop sophisticated mental models that serve to describe (and ultimately, explain) the motion of real-world objects.</div></p>
	<p>
<a name="p3"></a><div style="font-family:Times news roman; font-size: 15px;">In this lesson, we will investigate the words used to describe the motion of objects. That is, we will focus on the <em>language</em> of kinematics. The hope is to gain a comfortable foundation with the language that is used throughout the study of mechanics. We will study such terms as scalars, vectors, distance, displacement, speed, velocity and acceleration. These words are used with regularity to describe the motion of objects. Your goal should be to become very familiar with their meaning.<br>
